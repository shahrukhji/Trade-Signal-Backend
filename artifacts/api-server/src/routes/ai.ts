// ─── Standard AI analysis (user provides candles + their own API key) ─────────

async function callGemini(apiKey: string, model: string, prompt: string): Promise<string> {
  const url = `https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${apiKey}`;
  const res = await axios.post(url, {
    contents: [{ parts: [{ text: prompt }] }],
    generationConfig: { temperature: 0.3, maxOutputTokens: 4096 },
  });
  return res.data.candidates[0].content.parts[0].text;
}

async function callOpenAI(apiKey: string, model: string, prompt: string): Promise<string> {
  const res = await axios.post(
    "https://api.openai.com/v1/chat/completions",
    {
      model,
      messages: [
        { role: "system", content: "You are an expert stock market technical analyst." },
        { role: "user", content: prompt },
      ],
      temperature: 0.3,
      max_tokens: 4096,
    },
    { headers: { Authorization: `Bearer ${apiKey}`, "Content-Type": "application/json" } }
  );
  return res.data.choices[0].message.content;
}

async function callClaude(apiKey: string, model: string, prompt: string): Promise<string> {
  const res = await axios.post(
    "https://api.anthropic.com/v1/messages",
    {
      model,
      max_tokens: 4096,
      messages: [{ role: "user", content: prompt }],
    },
    {
      headers: {
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01",
        "Content-Type": "application/json",
      },
    }
  );
  return res.data.content[0].text;
}

// ─── Route: manual analyze (user-supplied candles + API key) ─────────────────

router.post("/analyze", async (req, res) => {
  try {
    const { provider, model, apiKey, stockName, timeframe, candles, indicators, patterns, levels } = req.body as {
      provider: "gemini" | "openai" | "claude";
      model: string;
      apiKey: string;
      stockName: string;
      timeframe: string;
      candles: Candle[];
      indicators: Record<string, unknown>;
      patterns: { candlestick: string[]; chart: string[] };
      levels?: Record<string, unknown>;
    };

    if (!provider || !apiKey || !candles) {
      res.status(400).json({ error: "provider, apiKey, and candles are required" });
      return;
    }

    const prompt = buildAnalysisPrompt({ stockName, timeframe, candles, indicators, patterns, levels });

    let rawText: string;
    switch (provider) {
      case "gemini": rawText = await callGemini(apiKey, model || "gemini-1.5-pro", prompt); break;
      case "openai": rawText = await callOpenAI(apiKey, model || "gpt-4o", prompt); break;
      case "claude": rawText = await callClaude(apiKey, model || "claude-3-5-sonnet-20241022", prompt); break;
      default:
        res.status(400).json({ error: "provider must be gemini, openai, or claude" });
        return;
    }

    const result = parseAIResponse(rawText);
    res.json({ success: true, provider, result });
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : "AI analysis failed";
    res.status(500).json({ error: msg });
  }
});

// ─── Route: Cross-Verify — algo engine vs. Gemini AI (Replit managed key) ────

router.post("/cross-verify", async (req, res) => {
  try {
    const {
      symbol,
      symboltoken,
      exchange = "NSE",
      interval = "FIFTEEN_MINUTE",
    } = req.body as {
      symbol: string;
      symboltoken?: string;
      exchange?: string;
      interval?: string;
    };

    if (!symbol) {
      res.status(400).json({ error: "symbol is required" });
      return;
    }

    // Resolve token
    let token = symboltoken;
    if (!token) {
      const stock = NIFTY50.find(s => s.symbol === symbol || s.symbol === `${symbol}-EQ`);
      token = stock?.token;
    }
    if (!token) {
      res.status(400).json({ error: `Token not found for symbol: ${symbol}` });
      return;
    }

    // 1. Fetch live candles from Angel One (reuse same helper as signals.ts)
    const candles = await fetchCandles(token, interval, exchange);

    if (candles.length < 20) {
      res.status(400).json({ error: `Insufficient candle data (got ${candles.length}, need 20+)` });
      return;
    }

    // 2. Run 20-indicator algorithm engine
    const algoResult = generateSignal(symbol, candles);
    const indicators = calculateIndicators(candles);
    const patterns = detectPatterns(candles);

    // Pass full indicators object — prompt builder accesses fields via `as any`
    const indicatorsPlain = indicators as unknown as Record<string, unknown>;

    const recentPrices = candles.slice(-20).map(c => c.close);

    // 3. Build cross-verify prompt and call Gemini (Replit-managed, no user key needed)
    const prompt = buildCrossVerifyPrompt({
      symbol: algoResult.symbol,
      interval,
      price: algoResult.entry,
      algoSignal: algoResult.signal,
      algoScore: algoResult.score,
      algoConfidence: algoResult.confidence,
      algoReasons: algoResult.reasons,
      indicators: indicatorsPlain,
      candlestickPatterns: patterns.candlestick ?? [],
      chartPatterns: patterns.chart ?? [],
      recentPrices,
    });

    const geminiResponse = await ai.models.generateContent({
      model: "gemini-2.5-flash",
      contents: [{ role: "user", parts: [{ text: prompt }] }],
      config: { temperature: 0.2, maxOutputTokens: 8192, responseMimeType: "application/json" },
    });

    const rawText = geminiResponse.text ?? "";
    const aiResult = parseAIResponse(rawText);

    res.json({
      success: true,
      symbol: algoResult.symbol,
      price: algoResult.entry,
      interval,
      algo: {
        signal: algoResult.signal,
        score: algoResult.score,
        confidence: algoResult.confidence,
        reasons: algoResult.reasons,
        entry: algoResult.entry,
        target1: algoResult.target1,
        target2: algoResult.target2,
        target3: algoResult.target3,
        stopLoss: algoResult.stopLoss,
        riskReward: algoResult.riskReward,
      },
      ai: aiResult,
      patterns: {
        candlestick: patterns.candlestick ?? [],
        chart: patterns.chart ?? [],
      },
      analyzedAt: new Date().toISOString(),
    });
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : "Cross-verify failed";
    console.error("[AI Cross-Verify] Error:", msg);
    res.status(500).json({ error: msg });
  }
});

